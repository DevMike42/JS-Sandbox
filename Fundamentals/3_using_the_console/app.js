// Log to console

// Log strings
console.log('Hello world');

// Log numbers
console.log(123);

// Log booleans
console.log(true);

// Log variables
var greeting = 'Hello';
console.log(greeting);

// Log arrays
console.log([1, 2, 3, 4]);

// Log Objects
console.log({ a: 1, b: 2 });

// Log Tables
console.table({ a: 1, b: 2 });

// Log Errors
console.error("This is an error");

// Clear console
console.clear();

// Warnings
console.warn("This is a warning");

// Timer
console.time("Timer");
console.log("Hello");
console.log("Hello");
console.log("Hello");
console.log("Hello");
console.log("Hello");
console.timeEnd("Timer");