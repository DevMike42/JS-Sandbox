const num1 = 100;
const num2 = 50;
let val;

// Simple math with numbers
val = num1 + num2; // Addition
console.log(val);

val = num1 * num2; // Multiplication
console.log(val);

val = num1 - num2; // Subtraction
console.log(val);

val = num1 / num2; // Division
console.log(val);

val = num1 % num2; // Modulus (Outputs remainder)
console.log(val);

// Math Object
val = Math.PI; // Outputs PI
console.log(val);

val = Math.E; // Outputs Euler's number
console.log(val);

val = Math.round(2.8); // Rounds up or down
console.log(val);

val = Math.ceil(2.5); // Rounds up
console.log(val);

val = Math.floor(2.8); // Rounds down
console.log(val);

val = Math.sqrt(64); // Square root
console.log(val);

val = Math.abs(-3); // Absulute number (positive version)
console.log(val);

val = Math.pow(8, 2); // Power of number (8 to the 2nd power)
console.log(val);

val = Math.min(2, 4, 45, 32, 12, 1, 56); // Minimum value (takes in comma seperated vaues)
console.log(val);

val = Math.max(2, 4, 45, 32, 12, 1, 56); // Maximum value (takes in comma seperated vaues)
console.log(val);

val = Math.random(); // Returns a random decimaled number
console.log(val);

val = Math.random() * 20; // Returns a random number between 0 and 19
console.log(val);

val = Math.random() * 20 + 1; // Returns a random number between 0 and 20
console.log(val);

val = Math.floor(Math.random() * 20 + 1); // Returns a random number between 0 and 20 and rounds it down
console.log(val);

